-- Production Batches
create table if not exists public.production_batches (
  id uuid primary key default gen_random_uuid(),
  menu_item_id bigint not null references public.menu_items(id) on delete restrict,
  quantity_produced numeric(10, 2) not null check (quantity_produced > 0),
  remaining_quantity numeric(10, 2) not null check (remaining_quantity >= 0),
  cost_per_unit numeric(10, 2) not null default 0 check (cost_per_unit >= 0),
  total_cost numeric(10, 2) generated always as (quantity_produced * cost_per_unit) stored,
  production_date date not null default CURRENT_DATE,
  created_at timestamptz not null default now(),
  created_by uuid references auth.users(id) on delete set null
);

-- Production Usage: Track batch consumption per order item (FIFO)
create table if not exists public.production_usage (
  id uuid primary key default gen_random_uuid(),
  order_id bigint not null references public.orders(id) on delete cascade,
  batch_id uuid not null references public.production_batches(id) on delete restrict,
  menu_item_id bigint not null references public.menu_items(id) on delete restrict,
  quantity numeric(10, 2) not null check (quantity > 0),
  created_at timestamptz not null default now()
);

-- Suppliers: Optional supplier records for ingredient purchasing.
create table if not exists public.suppliers (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  phone text,
  notes text,
  created_at timestamptz not null default now()
);

-- Ingredients: Raw materials used to produce menu items.
create table if not exists public.ingredients (
  id uuid primary key default gen_random_uuid(),
  name text not null unique,
  unit text not null default 'pcs' check (unit in ('g', 'kg', 'ml', 'liters', 'pcs', 'portion')),
  current_stock numeric(12, 3) not null default 0 check (current_stock >= 0),
  low_stock_threshold numeric(12, 3) not null default 0 check (low_stock_threshold >= 0),
  supplier_id uuid references public.suppliers(id) on delete set null,
  cost_per_unit numeric(12, 4) not null default 0 check (cost_per_unit >= 0),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Recipes: A normalized bill of materials for producing a menu item.
create table if not exists public.recipes (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  menu_item_id bigint unique references public.menu_items(id) on delete set null,
  expected_yield numeric(10, 2) not null default 1 check (expected_yield > 0),
  yield_unit text not null default 'pcs',
  notes text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Recipe Items: Ingredients and quantities required for a full recipe yield.
create table if not exists public.recipe_items (
  id uuid primary key default gen_random_uuid(),
  recipe_id uuid not null references public.recipes(id) on delete cascade,
  ingredient_id uuid not null references public.ingredients(id) on delete restrict,
  quantity_required numeric(12, 3) not null check (quantity_required > 0),
  unit text not null default 'pcs' check (unit in ('g', 'kg', 'ml', 'liters', 'pcs', 'portion')),
  created_at timestamptz not null default now(),
  unique(recipe_id, ingredient_id)
);

-- Ingredient movements: Full audit trail for restocks and production consumption.
create table if not exists public.ingredient_movements (
  id uuid primary key default gen_random_uuid(),
  ingredient_id uuid not null references public.ingredients(id) on delete restrict,
  movement_type text not null check (movement_type in ('restock', 'production', 'adjustment', 'waste')),
  quantity_delta numeric(12, 3) not null,
  unit_cost numeric(12, 4),
  production_batch_id uuid references public.production_batches(id) on delete set null,
  note text,
  created_at timestamptz not null default now()
);

-- Production ingredient usage: Exact ingredient cost breakdown per batch.
create table if not exists public.production_ingredient_usage (
  id uuid primary key default gen_random_uuid(),
  production_batch_id uuid not null references public.production_batches(id) on delete cascade,
  recipe_id uuid references public.recipes(id) on delete set null,
  ingredient_id uuid not null references public.ingredients(id) on delete restrict,
  quantity_used numeric(12, 3) not null check (quantity_used > 0),
  unit text not null,
  cost_per_unit numeric(12, 4) not null default 0 check (cost_per_unit >= 0),
  total_cost numeric(12, 2) generated always as (quantity_used * cost_per_unit) stored,
  created_at timestamptz not null default now()
);

