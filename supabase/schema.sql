-- Supplier Debts
create table if not exists public.debts (
  id uuid primary key default gen_random_uuid(),
  supplier_name text not null,
  item_supplied text not null,
  total_amount numeric(10, 2) not null check (total_amount > 0),
  paid_amount numeric(10, 2) not null default 0 check (paid_amount >= 0),
  remaining_balance numeric(10, 2) generated always as (total_amount - paid_amount) stored,
  status text not null default 'unpaid' check (status in ('unpaid', 'partial', 'paid')),
  transaction_date date not null default CURRENT_DATE,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Waiter Payments
create table if not exists public.waiter_payments (
  id uuid primary key default gen_random_uuid(),
  waiter_name text not null,
  amount numeric(10, 2) not null check (amount > 0),
  payment_type text not null check (payment_type in ('wage', 'tip', 'bonus', 'commission')),
  payment_date date not null default CURRENT_DATE,
  created_at timestamptz not null default now()
);

-- Payment Transactions
create table if not exists public.payment_transactions (
  id uuid primary key default gen_random_uuid(),
  order_id bigint references public.orders(id) on delete set null,
  amount numeric(10, 2) not null check (amount > 0),
  method text not null check (method in ('cash', 'mobile_money', 'card', 'credit')),
  status text not null default 'completed' check (status in ('pending', 'completed', 'failed', 'refunded')),
  reference_code text,
  transaction_date timestamptz not null default now()
);

