-- Production: Track daily food prepared by chef
create table if not exists public.production (
  id bigint generated by default as identity primary key,
  item_name text not null,
  quantity integer not null check (quantity > 0),
  cost_per_unit numeric(10, 2) not null check (cost_per_unit >= 0),
  total_value numeric(10, 2) generated always as (quantity * cost_per_unit) stored,
  created_at timestamptz not null default now()
);

-- Stock Opening: Items available at start of day
create table if not exists public.stock_opening (
  id bigint generated by default as identity primary key,
  item_name text not null,
  quantity integer not null check (quantity >= 0),
  recorded_date date not null default CURRENT_DATE,
  created_at timestamptz not null default now(),
  unique(item_name, recorded_date)
);

-- Stock Usage: Internal usage tracking
create table if not exists public.stock_usage (
  id bigint generated by default as identity primary key,
  person_name text not null,
  item_name text not null,
  quantity integer not null check (quantity > 0),
  usage_date date not null default CURRENT_DATE,
  created_at timestamptz not null default now()
);

-- Stock Closing: Items remaining at end of day
create table if not exists public.stock_closing (
  id bigint generated by default as identity primary key,
  item_name text not null,
  quantity integer not null check (quantity >= 0),
  recorded_date date not null default CURRENT_DATE,
  created_at timestamptz not null default now(),
  unique(item_name, recorded_date)
);

-- Deliveries: Track delivery records
create table if not exists public.deliveries (
  id bigint generated by default as identity primary key,
  customer_name text not null,
  location text not null,
  items text not null,
