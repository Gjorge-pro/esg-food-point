drop table if exists public.menu_items cascade;
drop table if exists public.categories cascade;
drop table if exists public.users cascade;

create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name text not null,
  parent_id bigint references public.categories(id) on delete cascade,
  created_at timestamptz not null default now()
);

create table if not exists public.users (
  id uuid primary key references auth.users(id) on delete cascade,
  name text not null,
  role text not null check (role in ('admin', 'manager', 'service_desk', 'waiter', 'kitchen')),
  created_at timestamptz not null default now()
);

create table if not exists public.menu_items (
  id bigint generated by default as identity primary key,
  name text not null,
  price numeric(10, 2) not null check (price >= 0),
  category_id bigint not null references public.categories(id) on delete restrict,
  available boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.orders (
  id bigint generated by default as identity primary key,
  order_type text not null check (order_type in ('dine_in', 'delivery')),
  customer_source text default 'walk_in' check (customer_source in ('walk_in', 'phone_order', 'online', 'delivery_app', 'other')),
  customer_name text,
  phone text,
  address text,
  table_number text,
  status text not null default 'pending' check (status in ('pending', 'accepted', 'cooking', 'ready', 'on_the_way', 'served', 'delivered')),
  payment_method text check (payment_method in ('cash', 'mobile_money', 'card')),
  payment_status text default 'pending' check (payment_status in ('pending', 'paid', 'failed')),
  estimated_time_minutes integer default 30 check (estimated_time_minutes > 0),
  created_at timestamptz not null default now()
);

create table if not exists public.order_items (
  id bigint generated by default as identity primary key,
  order_id bigint not null references public.orders(id) on delete cascade,
  menu_item_id bigint not null references public.menu_items(id) on delete restrict,
  quantity integer not null check (quantity > 0),
  instructions text,
  created_at timestamptz not null default now()
);

create table if not exists public.requests (
  id bigint generated by default as identity primary key,
  order_id bigint not null references public.orders(id) on delete cascade,
  type text not null check (type in ('call_waiter', 'request_bill')),
  status text not null default 'pending' check (status in ('pending', 'fulfilled')),
  created_at timestamptz not null default now()
);

create table if not exists public.feedback (
  id bigint generated by default as identity primary key,
  order_id bigint not null references public.orders(id) on delete cascade,
  rating integer not null check (rating >= 1 and rating <= 5),
  comment text,
  created_at timestamptz not null default now(),
  unique(order_id)
);

create index if not exists idx_categories_parent_id on public.categories(parent_id);
create index if not exists idx_menu_items_category_id on public.menu_items(category_id);
create index if not exists idx_orders_created_at on public.orders(created_at desc);
create index if not exists idx_orders_status on public.orders(status);
create index if not exists idx_orders_phone on public.orders(phone);
create index if not exists idx_order_items_order_id on public.order_items(order_id);
create index if not exists idx_requests_order_id on public.requests(order_id);
create index if not exists idx_requests_status on public.requests(status);
create index if not exists idx_feedback_order_id on public.feedback(order_id);

